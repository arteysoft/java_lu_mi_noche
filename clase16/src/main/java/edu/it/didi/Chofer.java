package edu.it.didi;

public class Chofer {
	public final String nombre;
	public final Boolean leGustaSuTrabajo;
	public final Integer cantidadAccidentes;
	
	public Chofer(String nombre, Boolean leGustaSuTrabajo, Integer cantidadAccidentes) {
		this.nombre = nombre;
		this.leGustaSuTrabajo = leGustaSuTrabajo;
		this.cantidadAccidentes = cantidadAccidentes;
	}
}
